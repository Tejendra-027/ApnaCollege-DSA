public class DataTypesInJava {
    public static void main(String[] args) {
        byte b = 8;
        System.out.println(b);

        char ch = 'a';
        System.out.println(ch);

        boolean var = true;
        System.out.println(var);

        float price = 20.5f;
        System.out.println(price);

        int number = 25;
        System.out.println(number);

        short n = 240;
        System.out.println(n);

        //long

        //double
    }
}
