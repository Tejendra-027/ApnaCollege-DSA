public class PrintTheLargestOf2Number {
    public static void main(String[] args) {
        int A = 2;
        int B = 1;

        if(A>B){
            System.out.println("A is larger");
        } else if(A == B){
            System.out.println("A and B are same");
        } else {
            System.out.println("B is larger");
        }
    }    
}
